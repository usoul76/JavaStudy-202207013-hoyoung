package j03_연산자;
//나머지: 홀,짝,배수
//증감: 선,후
//논리: &&,||,!
//조건: 조건 ? 결과1(참) : 결과2(거짓)
/*
 * Ctrl + space -> 자동완성
 */


public class Operation1 {

	public static void main(String[] args) {
		int num = 50;
		
		System.out.println(++num);
		System.out.println(num);
		System.out.println(num++);
		System.out.println(num);
		
		System.out.println(--num);
		System.out.println(num);
		System.out.println(num--);
		System.out.println(num);
		
		
		
	}

}
